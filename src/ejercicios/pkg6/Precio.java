    private void jTMaximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTMaximoActionPerformed
        // TODO add your handling code here:
        
        try{
        int maximo = Integer.parseInt(jTMaximo.getText());
        
        for (Producto pmax : MenuGeneral.productos){
            if(pmax.getPrecio() <= maximo){
                pmax.getPrecio();
                modelo.addRow(new Object[]{pmax.getCodigo(), pmax.getDescripcion(),
                    pmax.getPrecio(), pmax.getRubro(), pmax.getStock()});
            }
        }
        }catch(NumberFormatException nf){
            JOptionPane.showMessageDialog(this, "Solo ingrese numeros");
        }
    }//GEN-LAST:event_jTMaximoActionPerformed

